    @Override
    public String solvePart1() {
        String split[] = input.split("\n");
        for (String s : split) {
            String[] split2 = s.split("x");
            int x = IntegerConverter.fromString(split2[0]);
            int y = IntegerConverter.fromString(split2[1]);
            int z = IntegerConverter.fromString(split2[2]);
            presents.add(new Present(x, y, z));
        }
        int result = 0;
        for (Present p : presents) {
            result += p.getAmountNeeded();
        }
        return result + "";
    }

    @Override
    public String solvePart2() {
        int sum = 0;

        for (P.Present p : presents) {
            List<Integer> perimeters = p.getPerimeters();
            Collections.sort(perimeters);

            sum += perimeters.get(0);
            sum += p.getVolume();
        }
        return sum + "";
    }

    @RequiredArgsConstructor
    public class Present {
        private final int x;
        private final int y;
        private final int z;

        public List<Integer> getPerimeters() {
            List<Integer> perimeters = new ArrayList<>();

            perimeters.add(x * 2 + y * 2);
            perimeters.add(y * 2 + z * 2);
            perimeters.add(x * 2 + z * 2);

            return perimeters;
        }

        public int getAmountNeeded() {
            int result = 0;
            List<Integer> areas = getAreas();
            Collections.sort(areas);


            for (Integer i : areas)
                result += 2 * i;

            result += areas.get(0);
            return result;
        }

        public int getVolume() {
            return x * y * z;
        }

        private List<Integer> getAreas() {
            List<Integer> areas = new ArrayList<>();

            areas.add(x * y);
            areas.add(y * z);
            areas.add(x * z);

            return areas;
        }
    }
