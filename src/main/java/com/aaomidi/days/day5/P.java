    @Override
    public String solvePart1() {
        String[] split = input.split("\n");
        int answer = 0;
        for (String s : split) {
            char[] array = s.toCharArray();
            int count = 0;
            boolean req = false;
            boolean bad = false;
            char lastChar = 'ض';

            for (char c1 : array) {
                if (c1 == lastChar) req = true;

                if (vowels.contains(c1)) count++;

                lastChar = c1;
            }

            if (s.contains("ab") || s.contains("cd") || s.contains("pq") || s.contains("xy")) bad = true;

            if (!bad && req && count >= 3) {
                answer++;
            }

        }
        return answer + "";
    }

